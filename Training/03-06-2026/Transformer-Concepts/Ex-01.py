#  1. What is Query?
#  A Query is the word representation that asks for relevant information from other words.
#
#  2. What is Key?
#  A Key is the representation that helps match with the Query to decide importance.
#
#  3. What is Value?
#  A Value is the actual information/content returned after matching Query and Key.
#
#  4. Why is Self-Attention needed?
#  Self-Attention helps:
# Understand context
# Capture relationships between words
# Focus on important words in a sentence