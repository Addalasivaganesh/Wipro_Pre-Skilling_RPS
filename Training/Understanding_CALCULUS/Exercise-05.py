import numpy as np

A = np.array([
    [1, 2],
    [3, 4]
])

B = np.array([
    [5, 6],
    [7, 8]
])

#adding two matrices
print("Adding two matrices A + B: ", A + B)

#subtracting matrices
print("Subtracting two matrices A - B: ", A - B)

#Multiply matrices element wise
print("Multiplying two matrices A * B: ", A * B)