# Neural Network Components (One-line Purpose)
#
#
# nn.Linear()
# Performs a linear transformation (input × weights + bias) to map features.
#
#
# nn.ReLU() (Rectified Linear Unit)
# Applies non-linearity by converting negative values to zero.
#
#
# nn.Dropout()
# Randomly disables neurons during training to prevent overfitting.
#
#
# nn.BatchNorm1d() (Batch Normalization)
# Normalizes layer inputs to improve training stability and speed.
#
#
# nn.CrossEntropyLoss()
# Computes loss for classification by comparing predictions with true labels.
#
# 
# optim.Adam() (Adaptive Moment Estimation)
# Optimizer that updates weights using adaptive learning rates and momentum.
