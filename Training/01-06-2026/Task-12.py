# Mini Theory Assessment (Brief Explanations)
# 1. ReLU (Rectified Linear Unit)
# Activation function that outputs max(0, x), helping models learn faster by avoiding vanishing gradients.
#
# 2. Softmax
# Converts raw scores into probabilities that sum to 1, used in multi-class classification.
#
# 3. Cross Entropy Loss
# Measures difference between predicted probabilities and actual class; lower loss means better prediction.
#
# 4. Batch Size
# Number of samples processed before one weight update; smaller size → more updates.
#
# 5. Epoch
# One complete pass through the entire dataset during training.
#
# 6. Adam Optimizer (Adaptive Moment Estimation)
# Optimizer that combines momentum and adaptive learning rates for faster convergence.
#
# 7. Dropout
# Regularization technique that randomly turns off neurons during training to prevent overfitting.
#
# 8. Batch Normalization
# Normalizes inputs within a batch to stabilize learning and speed up training.
#
# 9. Vanishing Gradient
# Gradients become very small, causing slow or stopped learning in deep networks.
#
# 10. Exploding Gradient
# Gradients become very large, leading to unstable training and possible model divergence.